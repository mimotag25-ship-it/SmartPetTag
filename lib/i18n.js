function getStoredLang() {
  if (typeof localStorage !== 'undefined') {
    const stored = localStorage.getItem('spt_lang');
    if (stored) return stored;
  }
  if (typeof navigator !== 'undefined') {
    return navigator.language?.startsWith('en') ? 'en' : 'es';
  }
  return 'es';
}

